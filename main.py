first_number = input("enter the first number:")
operation = input("enter sign of the operation here:")
second_number = input("enter the second number here:")
if operation == "+":
    print(int(first_number)+ int(second_number))
if operation == "-" :
    print(int(first_number)- int(second_number))
if operation == "*" :
    print(int(first_number) * int(second_number))
if operation == "/" :
    print(int(first_number) / int(second_number))